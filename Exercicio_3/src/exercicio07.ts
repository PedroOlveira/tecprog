let i: number = 1; // Inicializa a variável de controle
let soma: number = 0; // Inicializa a variável para armazenar a soma

while (i <= 10) { // Continua o laço enquanto i for menor ou igual a 10
    soma += i; // Adiciona o valor de i à soma
    i++; // Incrementa i em 1
}

console.log("A soma dos inteiros de 1 a 10 é: " + soma); // Exibe o resultado
