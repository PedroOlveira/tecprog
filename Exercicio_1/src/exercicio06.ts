let anos: number, meses: number, dias: number, idadeEmDias: number;

anos = 25; // idade em anos
meses = 6; // idade em meses
dias = 15; // idade em dias

idadeEmDias = (anos * 365) + (meses * 30) + dias; // conversão da idade para dias

console.log("A idade em dias é: ", idadeEmDias); // mostra a idade em dias no console
